                    <h4 class="lead ">Benefits of Breastfeeding</h4>
                    <p class=""align = "justify">Breastfeeding your newborn has many benefits. Perhaps most important, breast milk is the perfect food for a baby's digestive system. It has the nutrients that a newborn needs, and it’s easily digested. Commercial formulas try to imitate breast milk, and come close, but can't match it exactly.

Breast milk has antibodies that help protect babies from many infections, including diarrhea and ear and lung infections. Breastfed babies are less likely to develop medical problems such as diabetes, high cholesterol, asthma, and allergies. Breastfeeding also might make a child less likely to become overweight.

Breastfeeding is great for moms too. It burns calories, so can help nursing moms lose the weight gained during pregnancy. Breastfeeding also may offer protection from breast cancer and ovarian cancer.

Some moms find breastfeeding easier and quicker than formula feeding — it needs no preparation, and you don't run out of breast milk in the middle of the night. Also, breastfeeding costs little. Nursing mothers do need to eat more and may want to buy nursing bras and pads, a breast pump, or other equipment. But these expenses are generally less than the cost of formula.

Breastfeeding meets a variety of emotional needs for both moms and babies. The skin-to-skin contact can enhance the emotional connection, and providing complete nourishment can help new moms feel confident in their ability to care for their newborn.</p>
                    
                        
